# LOGICAL OPERATORS

'''
Logical Operators are used to add extra conditions to a statement

These can be referred to as compound conditions because they combine two 
    true or false evaluations

Some logical operators are:
    and
    or
    not
'''

# Here is an example of a compound conditional statement
4<5 and 6<7     # <-- This will evaluate to True because 4 is less than 5,
                # and 6 is less than 7

# Similar to a non-compound statement:
    # It will result in a True/False boolean value
    # The result may be stored to a variable and used, or used directly

boolean_result = 4<5 and 6<7
print(boolean_result)